package oop.chapt5;

/**
 * 基本型（プリミティブ型）と参照型（後述）とリテラル 
 */
public class Sample1 {

    /** 基本型　論理値 リテラル true, false*/
    boolean bl = true;

    /** 基本型　16ビットUnicode文字 */
    char c = 'Ａ';

    /** 基本型　8ビット符号付整数 */
    byte b = 10;

    /** 基本型　16ビット符号付整数 */
    short s = 20;

    /** 基本型　32ビット符号付整数 */
    int i = 30;

    /** 基本型　64ビット符号付整数 */
    long l = 40;

    /** 基本型　32ビット浮動小数点数 */
    float f = 10.0f;

    /** 基本型　64ビット浮動小数点数 */
    double d = 10.0;         // = 1.0e1, = 0.1E2 

    /** 参照型　リテラル null */
    Sample1 sample1 = null; 
    String s2 =  "文字列リテラル";
    double d2 =  3.1412; //数値リテラル
    double d3 =  3.1412; //数値リテラル
}
